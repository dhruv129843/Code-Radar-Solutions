char bubbleSort(int arr[], int n){
    for(int pass=0;pass<n-1; pass++){
    for(int i=0; i<n-1-pass; i++){
        if(arr[i]>arr[i+1]){
            char temp= arr[i];
            arr[i]= arr[i+1];
            arr[i+1]=temp;
        }
        void printArray(char arr[],int n){
            for(int i=0;i<n;i++);
            printf("%c",arr[i]);
            printf("\n");
        }
    }
    }
}